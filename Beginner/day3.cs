// Day 3: Beginner Challenge #3 - Write a program to check if a number is even or odd.

//using System;

public class day3 
{
    public static void evenOrOdd(int num) 
    {
        if(num % 2 == 0) {
            Console.WriteLine("Number is Even.");
        }
        else {
            Console.WriteLine("Number is Odd");
        }
    }
}