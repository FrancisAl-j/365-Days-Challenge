//Day 4: Beginner Challenge #4 - Build a simple calculator for +, -, _, /.
using System;

public class day4
{
    public static void calculator(int x, int y)
    {
        Console.WriteLine(x + y);
        Console.WriteLine(x - y);
        Console.WriteLine(x * y);
        Console.WriteLine(x / y);
    } 
}
